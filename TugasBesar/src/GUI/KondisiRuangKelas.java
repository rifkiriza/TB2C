    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
                        try {
            int row = jTable1.rowAtPoint(evt.getPoint());

            String namaruang = jTable1.getValueAt(row, 0).toString();
            String panjang = jTable1.getValueAt(row, 1).toString();
            String lebar = jTable1.getValueAt(row, 2).toString();
            String jmlkursi = jTable1.getValueAt(row, 3).toString();
            String jmlpintu = jTable1.getValueAt(row, 4).toString();
            String jmljendela = jTable1.getValueAt(row, 5).toString();

            jTextField1.setText(String.valueOf(namaruang));
            jTextField2.setText(String.valueOf(panjang));
            jTextField3.setText(String.valueOf(lebar));
            jTextField4.setText(String.valueOf(jmlkursi));
            jComboBox1.setSelectedItem(String.valueOf(jmlpintu));
            jComboBox2.setSelectedItem(String.valueOf(jmljendela));
          jButton1.setVisible(true);
        jButton2.setVisible(false);
        jButton3.setVisible(true);
        jButton4.setVisible(true);
        jButton5.setVisible(true);
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jTable1MouseClicked

