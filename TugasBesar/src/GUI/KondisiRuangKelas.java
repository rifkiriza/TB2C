           public void klikInsert() {
        String namaruang = getJTextField1().getText();
        String panjang = getJTextField2().getText();
        String lebar = getJTextField3().getText();
        String jmlkursi = getJTextField4().getText();
        
        try {
            Statement statement = konek.GetConnection().createStatement();
            statement.executeUpdate("INSERT INTO kondisi (namaruang,panjang,lebar,jmlkursi,jmlpintu,jmljendela) VALUES ('" + namaruang + "','"
                    + panjang + "','" + lebar + "','" + jmlkursi + "','" + jComboBox1.getSelectedItem() + "','"+jComboBox2.getSelectedItem()+"');");
            statement.close();
            JOptionPane.showMessageDialog(null, "Berhasil Memasukan Data");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Memasukan data");
        }
    }

