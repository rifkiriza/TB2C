    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        try {
            int row = jTable1.rowAtPoint(evt.getPoint());

            String namaruang = jTable1.getValueAt(row, 0).toString();
            String lokasi = jTable1.getValueAt(row, 1).toString();
            String fakultas = jTable1.getValueAt(row, 2).toString();

            jTextField1.setText(String.valueOf(namaruang));
            jTextField2.setText(String.valueOf(lokasi));
            jTextField3.setText(String.valueOf(fakultas));
            jButton1.setVisible(false);
            jButton3.setVisible(true);
            jButton4.setVisible(true);

        } catch (Exception e) {
        }
    }//GEN-LAST:event_jTable1MouseClicked

