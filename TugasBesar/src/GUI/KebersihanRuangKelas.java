    public void klikInsert() {
        String namaruang = getJTextField1().getText();
        String sirkulasiudara = getJTextField2().getText();
        String nilaipencahayaan = getJTextField3().getText();
        String kelembapan = getJTextField4().getText();
        String suhu = getJTextField5().getText();
        try {
            Statement statement = konek.GetConnection().createStatement();
            statement.executeUpdate("INSERT INTO kebersihan (namaruang,sirkulasiudara,nilaipencahayaan,kelembapan,suhu) VALUES ('" + namaruang + "','"
                    + sirkulasiudara + "','" + nilaipencahayaan + "','" + kelembapan + "','" + suhu + "');");
            statement.close();
            JOptionPane.showMessageDialog(null, "Berhasil Memasukan Data");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Memasukan data");
        }
    }

