    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
                try {
            int row = jTable2.rowAtPoint(evt.getPoint());

            String namaruang = jTable2.getValueAt(row, 0).toString();
            String sirkulasiudara = jTable2.getValueAt(row, 1).toString();
            String nilaipencahayaan = jTable2.getValueAt(row, 2).toString();
            String kelembapan = jTable2.getValueAt(row, 3).toString();
            String suhu = jTable2.getValueAt(row, 4).toString();

            jTextField1.setText(String.valueOf(namaruang));
            jTextField2.setText(String.valueOf(sirkulasiudara));
            jTextField3.setText(String.valueOf(nilaipencahayaan));
            jTextField4.setText(String.valueOf(kelembapan));
            jTextField5.setText(String.valueOf(suhu));
            jButton1.setVisible(false);
            jButton4.setVisible(true);
            jButton5.setVisible(true);

        } catch (Exception e) {
        }
    
    }//GEN-LAST:event_jTable2MouseClicked
