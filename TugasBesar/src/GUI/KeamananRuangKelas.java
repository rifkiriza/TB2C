    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        try {
            int row = jTable1.rowAtPoint(evt.getPoint());

            String namaruang = jTable1.getValueAt(row, 0).toString();
            String kekokohan = jTable1.getValueAt(row, 1).toString();
            String kunci = jTable1.getValueAt(row, 2).toString();
            String keamanan = jTable1.getValueAt(row, 3).toString();

            jTextField1.setText(String.valueOf(namaruang));
            jTextField2.setText(String.valueOf(kekokohan));
            jTextField3.setText(String.valueOf(kunci));
            jTextField4.setText(String.valueOf(keamanan));
            jButton1.setVisible(false);
            jButton2.setVisible(true);
            jButton3.setVisible(true);
            jButton4.setVisible(true);
            jButton5.setVisible(true);

        } catch (Exception e) {
        }
    }//GEN-LAST:event_jTable1MouseClicked

