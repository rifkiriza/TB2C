    public void klikInsert() {
        String namaruang = getJTextField1().getText();
        String kekokohan = getJTextField2().getText();
        String kunci = getJTextField3().getText();
        String keamanan = getJTextField4().getText();
        try {
            Statement statement = konek.GetConnection().createStatement();
            statement.executeUpdate("INSERT INTO keamanan(namaruang,kekokohan,kunci,keamanan) "
                    + "VALUES ('" + namaruang + "','"+ kekokohan + "','" + kunci + "','"+ keamanan +"');");
            statement.close();
            JOptionPane.showMessageDialog(null, "Berhasil Memasukan Data");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Memasukan data");
        }
    }

